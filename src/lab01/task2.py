"""
Задание 2 - Сумма и среднее
Ввод: два числа (вещественные), допускаются точка или запятая
Вывод: sum=<...>; avg=<...> - значения печатать с 2 знаками
"""

# Получаем ввод от пользователя
input1 = input("a: ").replace(',', '.')  # Заменяем запятые на точки
input2 = input("b: ").replace(',', '.')  # Заменяем запятые на точки

# Преобразуем в вещественные числа
num1 = float(input1)
num2 = float(input2)

# Вычисляем сумму и среднее
total = num1 + num2
average = total / 2

# Форматируем вывод с двумя знаками после запятой
print(f"sum={total:.2f}; avg={average:.2f}")
