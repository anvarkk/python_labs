"""
Задание 6* - Подсчет участников по формату обучения
Ввод: число N, затем N строк формата "Фамилия Имя Возраст Формат_уч"
Формат_уч: True/False (или сокращения) - очный/заочный формат
Вывод: два числа через пробел - количество очников и заочников
"""

# Считываем количество участников
n = int(input())

# Инициализируем счетчики
count_onsite = 0  # очный формат
count_online = 0   # заочный формат

# Обрабатываем каждого участника
for _ in range(n):
    # Считываем строку и разбиваем на части
    data = input().split()
    
    # Формат обучения - последний элемент в строке
    format_str = data[-1].lower()
    
    # Проверяем формат обучения по первому символу
    if format_str.startswith('t'):  # True, Tru, T и т.д.
        count_onsite += 1
    else:  # False, F и т.д.
        count_online += 1

# Выводим результат
print(f"{count_onsite} {count_online}")
