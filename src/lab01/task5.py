"""
Задание 5 - Инициалы и длина строки
Ввод: ФИО одной строкой (могут быть лишние пробелы)
Вывод: инициалы (верхний регистр) и длина исходной строки без крайних пробелов
"""

# Получаем ввод
fio_input = input("ФИО: ")

# Убираем пробелы в начале и конце
fio_cleaned = fio_input.strip()

# Вычисляем длину строки без крайних пробелов
length = len(fio_cleaned)

# Разбиваем на слова (учитываем, что между словами могут быть multiple spaces)
words = fio_cleaned.split()

# Формируем инициалы: берем первый символ каждого слова и переводим в верхний регистр
